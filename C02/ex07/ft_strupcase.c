char	*ft_strupcase(char *str)
{
	int	i;
	int	n;

	n = 'a' - 'A';
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'a' && str[i] <= 'z')
			str[i] = str[i] - n;
		i ++;
	}
	return (str);
}
