char	*ft_strlowcase(char *str)
{
	int	i;
	int	n;

	n = 'a' - 'A';
	i = 0;
	while (str[i] != '\0')
	{
		if (str[i] >= 'A' && str[i] <= 'Z')
			str[i] = str[i] + n;
		i ++;
	}
	return (str);
}
